    private void logIn() {

        try {
            mUsername = usernameTextField.getText();
            mPassword = passwordTextField.getText();

            Statement usersStatement = mConnection.createStatement();
            ResultSet usersResultSet = usersStatement.executeQuery("SELECT * FROM users WHERE username='"
                    + mUsername + "' AND "
                    + "password='"
                    + mPassword + "'");

            if (usersResultSet.next()) {
                
                //Checks if they are admin or user
                mUserID = usersResultSet.getInt(1);
                System.out.println(mUserID);
                boolean isAdmin = usersResultSet.getBoolean("isAdmin");
                //Shows unique interface
                if (isAdmin) {
                    login.setVisible(false);
                    adminpage.setVisible(true);
                    adminpage.setSize(980, 575);
                    adminImportingInfo();
                } else {
                    login.setVisible(false);
                    userpage.setVisible(true);
                    userpage.setSize(980, 575);
                    clearUserInfo();
                    if (hasOrder()) {
                        restoreOrderInfoMainPage();
                    }
                }

            } else {
                Check.setText("Invalid Password/Code");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Start.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

