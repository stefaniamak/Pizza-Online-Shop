    private void showMenuCatalogue(JPanel panelNameTaken) {

//        setLayout(new BorderLayout());
        try {
            Statement stMenu = mConnection.createStatement();
            ResultSet rsMenu = stMenu.executeQuery("select * from menu" + " ORDER BY type desc");

            int column = 1;
            int x = 0;
            int y = 0;
            
            ActionListener menuItemActionListener = new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent event) {
                    JButton priceButton = (JButton) event.getSource();
                    MenuCatalogueItem menuCatalogueItem = (MenuCatalogueItem) priceButton.getParent();
                    // call function
                    addToCart(menuCatalogueItem);
                }
            };
                    

            while (rsMenu.next()) {
//                System.out.println(rsMenu.getString("name"));

                double price = rsMenu.getDouble("price");
                String product = rsMenu.getString("name");
                String type = rsMenu.getString("type");

                MenuCatalogueItem item = new MenuCatalogueItem(price, product, type, menuItemActionListener);

                GridBagConstraints box = new GridBagConstraints();

                if (column == 1) {

                    box.weighty = 0.5;

                    box.gridx = x;
                    box.gridy = y;
                    x = x + 2;
                    column++;
                } else {

                    box.gridx = x;
                    box.gridy = y;
                    x = x - 2;
                    y = y + 2;
                    column--;
                }

                panelNameTaken.add(item, box);
                //              item.setVisible(true);

            }

        } catch (SQLException ex) {
            Logger.getLogger(Start.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

